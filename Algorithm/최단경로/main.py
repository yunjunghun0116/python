# 첫 정점부터 해당 노드간의 가장 짧은 거리를 해당 배열에 업데이트 하는 과정을 거친다.

# 1. 첫 정점을 기준으로 배열 선언하여 첫 정점에서 각 정점까지의 거리를 저장
# 초기에는 0, 나머지는 무한대 -> inf 로 표현
# 우선순위 큐에 (첫 정점, 거리 0) 만 먼저넣는다

# 2. 우선순위 큐에서 노드를 꺼냄
# 처음에는 첫 정점만 저장되어있으므로 첫 정점이 꺼내진다
# 첫 정점에 인접한 노드들 각각에 대해 첫 정점에서 각 노드로 가는 거리와 현재 배열에 저장되어 있는 첫 정점에서 각 정점까지의 거리를 비교한다
