#   결과적으로 너비 우선 탐색 방식과 유사하게 첫 정점에 인접한 노드들을 순차적으로 방문
#   만약 배열에 기록된 현재까지 발견된 최단거리보다 더 긴 거리를 가진 노드의 경우 해당 노드와 인접한 노드간의 거리 계산

